def search(nums, target):
    size = len(nums)
    if size == 0:
        return False
    if size == 1:
        if nums[0] == target:
            return True
        else:
            return False

    first, last = 0, size - 1
    if nums[last] > nums[first]:
        pivot = 0
    else:
        pivot = None
        while first < last:
            mid = int((first + last) / 2)
            num = nums[mid]
            if num > nums[mid + 1]:
                pivot = mid + 1
                break

            if last != size - 1 and nums[last] > nums[last + 1]:
                pivot = last + 1
                break
            if num < nums[last]:
                last = mid
            elif num > nums[last]:
                first = mid
            else:
                last = last - 1

        if pivot is None:
            pivot = first

    first, last = pivot, size - 1
    if pivot > 0 and nums[0] <= target <= nums[pivot - 1]:
        first, last = 0, pivot - 1

    while first <= last:
        mid = int((first + last) / 2)
        num = nums[mid]
        if num == target:
            return True
        if num < target:
            first = mid + 1
        else:
            last = mid - 1

    return False


